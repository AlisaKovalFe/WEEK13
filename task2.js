function add() 
{
    let a = prompt('Ввведите первое число', 'Первое число');
    let b = prompt('Ввведите второе число', 'Второе число');
    alert(+a + +b);
}

function subtract()
{
    let a = prompt('Ввведите первое число', 'Первое число');
    let b = prompt('Ввведите второе число', 'Второе число');
    alert(a - b)
}

function multiple()
{
    let a = prompt('Ввведите первое число', 'Первое число');
    let b = prompt('Ввведите второе число', 'Второе число');
    alert(a * b)
}

function division()
{
    let a = prompt('Ввведите первое число', 'Первое число');
    let b = prompt('Ввведите второе число', 'Второе число');
    alert(a / b)
}